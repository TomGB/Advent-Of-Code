function calculateScore(strategy) {
	// Define an object that maps the possible choices to their corresponding scores
	const choices = {
		A: 1, // Rock
		B: 2, // Paper
		C: 3, // Scissors
	};

	// Split the strategy into an array of rounds
	const rounds = strategy.split("\n");

	// Initialize the total score to 0
	let totalScore = 0;

	// Loop through each round
	for (const round of rounds) {
		// Get the opponent's choice and your choice for this round
		const [opponent, you] = round.split(" ");

		// Calculate the score for this round based on the outcome
		if (opponent === you) {
			// The round is a draw
			totalScore += 3;
		} else if (
			(opponent === "A" && you === "B") ||
			(opponent === "B" && you === "C") ||
			(opponent === "C" && you === "A")
		) {
			// You win
			totalScore += 6 + choices[you];
		} else {
			// You lose
			totalScore += choices[you];
		}
	}

	// Return the total score
	return totalScore;
}

